/** Content categories for prompt routing */
export type SheetCategory =
  | 'dsa'
  | 'ml'
  | 'llm'
  | 'system-design'
  | 'os'
  | 'dbms'
  | 'networks'
  | 'devops'
  | 'languages';

/** Supported programming languages for code generation */
export type CodeLanguage = 'python' | 'java' | 'cpp' | 'javascript' | 'go' | 'rust';

